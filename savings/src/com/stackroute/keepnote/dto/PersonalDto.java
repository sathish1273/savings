package com.stackroute.keepnote.dto;

public class PersonalDto {
	
	private double openingBalance;
	private double expenses;
	private double deposits;
	private double loansSanctioned;
	private double spLoansSanctioned;
	
	public double getOpeningBalance() {
		return openingBalance;
	}
	public void setOpeningBalance(double openingBalance) {
		this.openingBalance = openingBalance;
	}
	public double getExpenses() {
		return expenses;
	}
	public void setExpenses(double expenses) {
		this.expenses = expenses;
	}
	public double getDeposits() {
		return deposits;
	}
	public void setDeposits(double deposits) {
		this.deposits = deposits;
	}
	public double getLoansSanctioned() {
		return loansSanctioned;
	}
	public void setLoansSanctioned(double loansSanctioned) {
		this.loansSanctioned = loansSanctioned;
	}
	public double getSpLoansSanctioned() {
		return spLoansSanctioned;
	}
	public void setSpLoansSanctioned(double spLoansSanctioned) {
		this.spLoansSanctioned = spLoansSanctioned;
	}
	
	
	

}
