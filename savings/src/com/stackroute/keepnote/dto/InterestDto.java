package com.stackroute.keepnote.dto;

public class InterestDto {
		private int interestRate;
		private String status;
		private String interestType_id;
		public int getInterestRate() {
			return interestRate;
		}
		public void setInterestRate(int interestRate) {
			this.interestRate = interestRate;
		}
		public String getStatus() {
			return status;
		}
		public void setStatus(String status) {
			this.status = status;
		}
		public String getInterestType_id() {
			return interestType_id;
		}
		public void setInterestType_id(String interestType_id) {
			this.interestType_id = interestType_id;
		}
		
		
}
